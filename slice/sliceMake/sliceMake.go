package main

func main() {
	s := make([] int, 19)
	s[9] = 10;

	// vai ter 10 elementos mais vai ter 20 posições o array criado
	// capacidade
	// tamanho
	len(s)
	cap(s)

	s := make([] int, 10, 20)
	s = append(s, 1,2,3,4,5,56)
	// ele vai referenciando quando vai crescendo adicionando mais ele vai dobrando de tamanho
}